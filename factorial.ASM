	AREA RESET, CODE
	ENTRY
	LDR R0, =IN
	LDR R5, =OUT            ;MEMORY LOCATION WHERE FACTORIAL HAS TO BE STORED
	LDR R4, [R0]          
	MOV R3, #1               ;STORES RESULT OF FACTORIAL OF NUMBER
	MOVS R2, R4
    BEQ SKIP
	BL FACTORIAL
SKIP STR R3, [R5]
STOP B STOP

FACTORIAL
GO	MUL R3,R2,R3
	SUBS R2,R2,#1
	BNE GO
	MOV PC,LR
IN  DCD &6
	AREA MEMORY, DATA
OUT DCD &0
	END
	AREA RESET, CODE
	ENTRY
	LDR R0, =IN
	LDR R5, =OUT
	LDR R1,[R0]            ;Nth FIBONACCI NO.
	MOV R2,#1              ;FIRST FIBONACCI NO.
	MOV R3,#1              ;SECOND FIBONACCI NO.
	SUBS R1,R1,#2          ;NO. OF ADDITIONS
	BLE SKIP
	BL FIBO
SKIP STR R3,[R5]
STOP B STOP

FIBO
GO	ADD R4,R2,R3
	MOV R2,R3
	MOV R3,R4 
	SUBS R1,#1
	BNE GO
	MOV PC,LR
IN DCD 3
	AREA MEM, DATA
OUT DCD &0
	END